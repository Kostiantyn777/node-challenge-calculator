module.exports = {
  addNumbers: function (value1, value2) {
    const result = value1 + value2;
    return result.toString();
  },
  
  substractNumbers: function (value1, value2) {
    const result = value1 - value2;
    return result.toString();
  },
  
  multiplyNumbers: function (value1, value2) {
    const result = value1 * value2;
    return result.toString();
  },

  divideNumbers: function (value1, value2) {
    const result = value1 / value2;
    return result.toString();
  },
};
